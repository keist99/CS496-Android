    @Override
    public void onPlacesSuccess(final List<Place> places) {
        runOnUiThread(new Runnable() {
            @Override
            public void run() {
                for (noman.googleplaces.Place place : places) {
                    LatLng latLng = new LatLng(place.getLatitude(), place.getLongitude());
                    String markerSnippet = getCurrentAddress(latLng);
                    MarkerOptions markerOptions = new MarkerOptions();
                    markerOptions.position(latLng);
                    markerOptions.title(place.getName());
                    markerOptions.snippet(markerSnippet);
                    Marker item = mGoogleMap.addMarker(markerOptions);
                    previous_marker.add(item);
                }

                //중복 마커 제거
                HashSet<Marker> hashSet = new HashSet<Marker>();
                hashSet.addAll(previous_marker);
                previous_marker.clear();
                previous_marker.addAll(hashSet);

                curLat = location.getLatitude();
                curLong = location.getLongitude();

                //가까운 순으로 정렬
                //...
                places.sort(new Comparator<Place>() {
                    @Override
                    public int compare(Place m1, Place m2) {
                        double lat_dis_1 = m1.getLatitude() - curLat;
                        double long_dis_1 = m1.getLongitude() - curLong;
                        double dis_1 = lat_dis_1 * lat_dis_1 + long_dis_1 * long_dis_1;

                        double lat_dis_2 = m2.getLatitude() - curLat;
                        double long_dis_2 = m2.getLongitude() - curLong;
                        double dis_2 = lat_dis_2 * lat_dis_2 + long_dis_2 * long_dis_2;

                        return dis_1 < dis_2 ? -1 : 1;
                    }
                });

                LatLng curLL = new LatLng(location.getLatitude(), location.getLongitude());
                for (noman.googleplaces.Place place : places) {
                    LatLng nextLL = new LatLng(place.getLatitude(), place.getLongitude());
                    mGoogleMap.addPolyline(new PolylineOptions().add(curLL, nextLL).width(10).color(Color.RED));
                    curLL = nextLL;
                }
            }
        });
    }

